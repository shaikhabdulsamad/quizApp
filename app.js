var startSection = document.getElementById('start-section');
var quizSection = document.getElementById('quiz-section');
var resultSection = document.getElementById('result-section');
var subName = document.getElementById('sub-name')
var userName = document.getElementById('user-name');
var nameText = document.getElementById('name-text');
var subjects = document.getElementById('subjects');
var questionText = document.getElementById('question-text');
var choicesContainer = document.getElementById('choices-container');
var resultText = document.getElementById('result-text');
var resultUser = document.getElementById('result-user');
var nextBtn = document.getElementById('next-btn');
var backBtn = document.getElementById('back-btn');
var skipBtn = document.getElementById('skip-btn');
var timerText = document.getElementById('timer-text');
var qcounter = document.getElementById('qcounter');
var startBtn = document.getElementById('startBtn');
var currentIndex = 0;
var score = 0;
var questionCounter = 1;
var timer = 98;

startBtn.addEventListener('click', function(){
	if(subjects.value !== ''){
		start();
	} 
	else{alert('Please select any subject');}
	
});

function timeCounter(){

setInterval(function(){
	
			timerText.textContent =  (timer < 10)? "0"+timer : timer;
			if(timer >=  0){
			 	timer--
			 }
		else{
			showResult()
		}
	}, 1000)
}


function start(){
	timeCounter()

	if(userName.value == ''){
		nameText.textContent = 'Welcome!'
	}
	else{
		nameText.textContent = `Welcome ${userName.value.toUpperCase()}!`
	}
	
	startSection.style.display = 'none';
	quizSection.style.display = 'block';

  quizLoad()

}



function quizLoad(){

 subName.textContent = `${subjects.value.toUpperCase()} QUIZ`
	
qcounter.textContent = `Question No. ${questionCounter++} / ${questions[subjects.value].length}`

var currentQues = questions[subjects.value][currentIndex];

questionText.textContent = currentQues.question;

choicesContainer.innerHTML = '';

currentQues.options.forEach(function(choice){

var choiceBtn = document.createElement('button');
choiceBtn.classList.add("answer-key");
choiceBtn.textContent = choice;
choiceBtn.addEventListener('click', function(){
	checkAnswer(choice, choiceBtn);
})


choicesContainer.appendChild(choiceBtn);


})

}



function checkAnswer(choice, choiceBtn){

var currentQues = questions[subjects.value][currentIndex];

if(choiceBtn){
  if(choice !== currentQues.correctAnswer){
    choiceBtn.style.background = 'red'; 
  
  }
  else{
    score++;
    choiceBtn.style.background = 'green'; 
  }
  currentIndex++;
}
else{
  currentIndex++;
}


if(currentIndex < questions[subjects.value].length){
  
	setTimeout(quizLoad, 200);

}
else{
	setTimeout(showResult, 300);
	
}
}


function showResult(){

quizSection.style.display = 'none';
startSection.style.display = 'none';
resultSection.style.display = 'block';

	resultText.innerHTML = `Your score in <br> ${subjects.value.toUpperCase()} QUIZ <br> is ${score} out of ${questions[subjects.value].length}!`;
	nextBtn.style.display = 'none';
	skipBtn.style.display = 'none';
	backBtn.style.display = 'block';
	qcounter.parentElement.style.display = 'none';


	if(userName.value == ''){
		resultUser.textContent = 'Thanks!'
	}
	else{
		resultUser.innerHTML = `Thanks <br> ${userName.value.toUpperCase()}!`
	}
}


nextBtn.addEventListener('click', checkAnswer);

backBtn.addEventListener('click', function(){
location.reload();

})

skipBtn.addEventListener('click', function(){
	location.reload();
})


quizSection.style.display = 'none';
resultSection.style.display = 'none';

